package com.egen.finalproject.interceptors.locationService;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;

@Component
public class LocationServiceInterceptorAppConfig implements WebMvcConfigurer {
    @Autowired
    LocationServiceInterceptor locationServiceInterceptor;

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(locationServiceInterceptor).addPathPatterns("/**");
    }
}


